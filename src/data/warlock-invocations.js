export function clampWarlockLevel(level) {
  const numeric = Number.parseInt(level, 10);
  if (!Number.isFinite(numeric)) return 0;
  return Math.max(0, Math.min(20, numeric));
}

export function getWarlockInvocationCountByLevel(level, table = []) {
  return Number(table[clampWarlockLevel(level)] || 0);
}

export function getWarlockInvocationById(collection = [], id = "") {
  return (collection || []).find((invocation) => invocation?.id === id) || null;
}

export function getWarlockPactBoonById(id = "") {
  return WARLOCK_PACT_BOONS_5E.find((boon) => boon.id === id) || null;
}

export function getWarlockInvocationOptions(invocations = [], level = 0, { pactBoonIds = [], invocationIds = [], cantripIds = [] } = {}) {
  const safeLevel = clampWarlockLevel(level);
  const pactSet = new Set((pactBoonIds || []).filter(Boolean));
  const invocationSet = new Set((invocationIds || []).filter(Boolean));
  const cantripSet = new Set((cantripIds || []).filter(Boolean));

  return (invocations || [])
    .filter((invocation) => safeLevel >= Number(invocation?.minLevel || 1))
    .filter((invocation) => !invocation?.pactPrerequisite || pactSet.has(invocation.pactPrerequisite))
    .filter((invocation) => !invocation?.invocationPrerequisite || invocationSet.has(invocation.invocationPrerequisite))
    .filter((invocation) => !invocation?.cantripPrerequisite || cantripSet.has(invocation.cantripPrerequisite))
    .map((invocation) => ({
      ...invocation,
      value: invocation.id,
      label: invocation.label,
    }))
    .sort((a, b) => {
      const groupCompare = String(a.group || "").localeCompare(String(b.group || ""), "pt-BR");
      return groupCompare || String(a.label || "").localeCompare(String(b.label || ""), "pt-BR");
    });
}

export function formatWarlockInvocationPrerequisites(invocation = {}) {
  const parts = [];
  const level = Number(invocation.minLevel || 0);
  if (level > 1) parts.push(`Nível ${level}+`);
  if (invocation.pactPrerequisite) {
    parts.push(`Dádiva do Pacto: ${PACT_SHORT_LABELS[invocation.pactPrerequisite] || invocation.pactPrerequisite}`);
  }
  if (invocation.invocationPrerequisiteLabel) parts.push(`Invocação: ${invocation.invocationPrerequisiteLabel}`);
  if (invocation.cantripPrerequisiteLabel) parts.push(`Truque: ${invocation.cantripPrerequisiteLabel}`);
  if (invocation.spellPrerequisite) parts.push(`Magia: ${invocation.spellPrerequisite}`);
  if (invocation.prerequisite) parts.push(invocation.prerequisite);
  return parts.join(" • ") || "Sem pré-requisito adicional";
}
